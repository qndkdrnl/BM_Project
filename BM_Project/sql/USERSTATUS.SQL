CREATE TABLE USERSTATUS(
USER_ID VARCHAR2(11) NOT NULL,
USER_STATUS NUMBER(2) NOT NULL,
NUM_OF_POSSIBLE_CHECKED_OUT NUMBER(3) NOT NULL,
NUM_OF_CHECKED_OUT_BOOK NUMBER(3) NOT NULL,
TOTAL_LATE_FEE NUMBER(5) NULL,
INSERTED_DATE DATE NOT NULL,
MODIFIED_DATE DATE NULL,
CONSTRAINT PK_USERSTATUS PRIMARY KEY(USER_ID),
CONSTRAINT FK_USERSTATUS FOREIGN KEY(USER_ID)
REFERENCES USERINFO(USER_ID)
);
