CREATE TABLE BOOKINFO(
TITLE VARCHAR2(300) NOT NULL,
AUTHOR VARCHAR2(300) NOT NULL,
TRANSLATORS VARCHAR2(300) NULL,
PUBLISHER VARCHAR2(90) NOT NULL,
PUBDATE VARCHAR2(10) NOT NULL,
ISBN10 VARCHAR2(10) NOT NULL,
ISBN13 VARCHAR2(13) NOT NULL,
BOOK_ID VARCHAR2(30) NOT NULL,
BOOK_LOC VARCHAR2(90) NOT NULL,
IMPORT_DATE VARCHAR2(10) NOT NULL,
EXPORT_DATE VARCHAR2(10) NULL,
THUMBNAIL_URL VARCHAR2(500) NULL,
INSERTED_DATE DATE NOT NULL,
MODIFIED_DATE DATE NULL,
CONSTRAINT PK_BOOKINFO PRIMARY KEY(BOOK_ID)
);
