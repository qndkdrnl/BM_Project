CREATE TABLE CHECKINGOUT(
NO NUMBER(10) NOT NULL,
USER_ID VARCHAR2(11) NOT NULL,
BOOK_ID VARCHAR2(30) NOT NULL,
CHECKED_OUT_DATE VARCHAR2(10) NOT NULL,
RETURN_DATE VARCHAR2(10) NOT NULL,
RETURNED_DATE VARCHAR2(10) NULL,
LATE_FOR_RETURN NUMBER(2) NOT NULL,
LATE_FEE NUMBER(5) NOT NULL,
PAID_FEE NUMBER(5) NOT NULL,
INSERTED_DATE DATE NOT NULL,
MODIFIED_DATE DATE NULL,
CONSTRAINT PK_CHECKINGOUT PRIMARY KEY(NO),
CONSTRAINT FK_CHECKINGOUT FOREIGN KEY(USER_ID)
REFERENCES USERINFO(USER_ID),
CONSTRAINT FK_CHECKINGOUT1 FOREIGN KEY(BOOK_ID)
REFERENCES BOOKINFO(BOOK_ID)
);
