CREATE TABLE USERINFO(
USER_ID VARCHAR2(11) NOT NULL,
USER_NAME VARCHAR2(24) NOT NULL,
USER_PHONENUMBER VARCHAR2(13) NOT NULL,
DATE_OF_ADMISSION VARCHAR2(10) NOT NULL,
USER_ADDRESS VARCHAR2(300) NOT NULL,
USER_CLASS NUMBER(2) NOT NULL,
INSERTED_DATE DATE NOT NULL,
MODIFIED_DATE DATE NULL,
CONSTRAINT PK_USER PRIMARY KEY(USER_ID)
);
